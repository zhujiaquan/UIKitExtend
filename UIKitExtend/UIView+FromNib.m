@implementation UIView (FromNib)

///////////////////////////////////////////////////////////////////////////////////////////////////
- (id)initWithNibName:(NSString *)nibNameOrNil bundle:(NSBundle *)nibBundleOrNil
{
	UINib *nib = [UINib nibWithNibName:nibNameOrNil bundle:nibBundleOrNil];
    self = [[[nib instantiateWithOwner:nil options:nil] objectAtIndex:0] retain];
    if (self)
    {
        // Initialization code

    }
    return self;
}

///////////////////////////////////////////////////////////////////////////////////////////////////
+ (id)viewFromNibName:(NSString *)nibNameOrNil bundle:(NSBundle *)nibBundleOrNil
{
    return AMAutorelease( [[[self class] alloc] initWithNibName:nibNameOrNil bundle:nibBundleOrNil] );
}

///////////////////////////////////////////////////////////////////////////////////////////////////
+ (id)viewFromNib
{
    return [[self class] viewFromNibName:[[self class] description] bundle:nil];
}

@end
